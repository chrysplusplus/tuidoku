def printNumber(num: tuple[str, ...]):
    num_str = '\n'.join(num)
    print(num_str, end = "\n\n")

def printSetN(nums: list[tuple[str, ...]]):
    n = len(nums[0])
    lines = []
    for i in range(n):
        lines.append(' '.join(num[i] for num in nums[0:5]))

    if n > 1:
        lines.append("")

    for i in range(n):
        lines.append(' '.join(num[i] for num in nums[5:10]))

    print('\n'.join(lines))

